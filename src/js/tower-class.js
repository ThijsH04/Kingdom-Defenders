class TowerClass {
    constructor(name, description, color) {
        this.name = name
        this.description = description
        this.color = color

        this.damageMod = 1
        this.healthMod = 1
        this.speedMod = 1
        this.rangeMod = 1
        this.costMod = 1
        this.effectMod = 1
    }
}